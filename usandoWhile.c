#include <stdio.h>

int main() {
    
    int numero = 1; // Inicializa o número com 1
    
    while (numero <= 100) {

        printf("Numero: %d\n", numero); // Imprime o número atual
        numero++; // Incrementa o número

    }

   return 0; // Retorna 0 para indicar que o programa terminou corretamente
}